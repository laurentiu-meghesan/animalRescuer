package org.fasttrackit;

public class Animal {

    String name;
    int age;
    int healthLevel;
    int hungerLevel;
    int spiritLevel;
    String favoriteFood;
    String favoriteRecreation;
    int fatLevel;
    String gender;
    int energyLevel;

    public Animal(String name, int age, String gender) {
        this.name = name;
        this.age = age;
        this.gender = gender;
    }



}
